def classify_topic(text: str, title: str = "") -> str:
    blob = f"{title} {title} {text}".lower()  # title weighted x2
    best, best_score = "General", 0
    for topic, terms in TOPIC_GAZETTEER.items():
        score = sum(blob.count(t) for t in terms)
        if score > best_score:
            best, best_score = topic, score
    return "Vision 2030" if best == "Energy_Vision" else best


# ------------------------------------------------------------------ entities ---
# Compact gazetteers for high-precision Saudi entities + generic patterns.
ORG_GAZETTEER = [
    "Aramco", "SABIC", "PIF", "Public Investment Fund", "SAMA", "Tadawul", "CMA",
    "STC", "NEOM", "ROSHN", "Qiddiya", "SDAIA", "GASTAT", "ZATCA", "MISA",
    "أرامكو", "سابك", "صندوق الاستثمارات العامة", "ساما", "تداول", "هيئة السوق المالية",
    "نيوم", "روشن", "القدية", "سدايا", "زاتكا", "وزارة الاستثمار", "البنك المركزي",
]
LOC_GAZETTEER = [
    "Saudi Arabia", "Riyadh", "Jeddah", "Makkah", "Mecca", "Medina", "Dammam",
    "Eastern Province", "Gulf", "GCC", "Red Sea",
    "السعودية", "الرياض", "جدة", "مكة", "المدينة", "الدمام", "الخليج", "البحر الأحمر",
    "المنطقة الشرقية",
]
# Capitalized multi-word sequences = candidate proper nouns (English text).
_PROPER_RE = re.compile(r"\b([A-Z][a-z]+(?:\s+[A-Z][a-z]+){1,3})\b")
_TITLE_HINT = ("Prince", "King", "Minister", "Dr.", "Sheikh", "الأمير", "الملك", "الوزير", "الدكتور", "الشيخ")


def extract_entities(text: str, title: str = "") -> dict:
    blob = f"{title}. {text}"
    orgs = sorted({g for g in ORG_GAZETTEER if g in blob})
    locs = sorted({g for g in LOC_GAZETTEER if g in blob})
    people = []
    for cand in _PROPER_RE.findall(blob):
        if cand in orgs or cand in locs:
            continue
        # Treat as person if preceded by a title hint anywhere nearby.
        if any(h in blob[max(0, blob.find(cand) - 20): blob.find(cand)] for h in _TITLE_HINT):
            people.append(cand)
    people = sorted(set(people))[:10]
    return {"organizations": orgs[:10], "people": people, "locations": locs[:10]}


# ----------------------------------------------------------------- sentiment ---
_POS = set("growth gain rise boost success record launch agreement win award approve expand profit surge improve منتدى نجاح إطلاق نمو ارتفاع اتفاق فوز جائزة موافقة توسع أرباح تحسن إنجاز".split())
_NEG = set("loss decline fall crisis attack war death cut deficit risk warning ban fraud delay drop خسارة تراجع أزمة هجوم حرب وفاة عجز خطر تحذير حظر احتيال تأخير انخفاض".split())


def analyze_sentiment(text: str) -> str:
    toks = [t.lower() for t in _tokens(text)]
    pos = sum(1 for t in toks if t in _POS)
    neg = sum(1 for t in toks if t in _NEG)
    if pos > neg and pos > 0:
        return "positive"
    if neg > pos and neg > 0:
        return "negative"
    return "neutral"


# ---------------------------------------------------------------- importance ---
def importance(item: dict, source_priority: int = 3) -> float:
    """0-100 heuristic: source priority + content depth + entity richness."""
    score = (6 - max(1, min(source_priority, 5))) * 10  # p1 -> 50, p5 -> 10
    content_len = len((item.get("content") or ""))
    score += min(content_len / 100, 30)  # up to +30 for substance
    ent = item.get("entities") or {}
    score += min(sum(len(v) for v in ent.values()) * 2, 20)  # up to +20
    return round(min(score, 100), 1)


# -------------------------------------------------------------------- enrich ---
def enrich(item: dict, source: dict | None = None) -> dict:
    """Add intelligence fields to a normalized article dict (in place)."""
    source = source or {}
    text = item.get("content") or item.get("summary") or ""
    title = item.get("title") or ""

    item["language"] = detect_language(f"{title} {text}")
    item["keywords"] = extract_keywords(f"{title} {text}", 10)
    item["entities"] = extract_entities(text, title)
    item["sentiment"] = analyze_sentiment(f"{title} {text}")
    # Only override category with the classifier when the source didn't pin one
    # to a meaningful topic (sources.json category is a coarse bucket).
    item["category"] = classify_topic(text, title) or item.get("category") or "General"
    if not item.get("summary"):
        item["summary"] = summarize(text, title)
    item["importance_score"] = importance(item, source.get("priority", 3))
    return item
