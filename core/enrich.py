"""Intelligence layer (Phase 3) — pure Python, bilingual (Arabic + English).

No ML deps, no API keys: runs fully offline on the Saudi node. Produces
language, summary, keywords, entities, topic/category, sentiment, and an
importance score for each article.

LLM upgrade path: set an `llm_summarizer` callable via set_llm_summarizer()
and summaries will be generated by it instead of the extractive fallback —
nothing else in the pipeline changes.
"""
from __future__ import annotations

import re
from collections import Counter

# ----------------------------------------------------------------- language ---
_ARABIC_RE = re.compile(r"[؀-ۿ]")


def detect_language(text: str) -> str:
    if not text:
        return "unknown"
    arabic = len(_ARABIC_RE.findall(text))
    letters = len(re.findall(r"[^\W\d_]", text, flags=re.UNICODE)) or 1
    return "ar" if arabic / letters > 0.3 else "en"


# ------------------------------------------------------------------ stopwords ---
_EN_STOP = set(
    "the a an and or but of to in on for with at by from as is are was were be been "
    "this that these those it its has have had will would can could should may might "
    "said says new news report reports year years today after before over under more "
    "most some any all not no into out up down about than then so such we you they he "
    "she his her their our your i me my".split()
)
_AR_STOP = set(
    "في من على إلى عن أن إن أو و ثم قد كان كانت يكون التي الذي الذين هذا هذه ذلك تلك "
    "ما لا لم لن هو هي هم نحن أنت كل بعض غير بين خلال بعد قبل عند منذ حتى مع كما حيث "
    "اليوم أمس قال قالت وقال أكد أوضح أعلن الى انه وانه يوم العام السعودية المملكة".split()
)
_STOP = _EN_STOP | _AR_STOP

_TOKEN_RE = re.compile(r"[؀-ۿA-Za-z][؀-ۿA-Za-z\-']{2,}")


def _tokens(text: str) -> list[str]:
    return [t for t in _TOKEN_RE.findall(text or "")]


def extract_keywords(text: str, limit: int = 10) -> list[str]:
    counts = Counter(
        t.lower() for t in _tokens(text) if t.lower() not in _STOP and len(t) > 2
    )
    return [w for w, _ in counts.most_common(limit)]


# ------------------------------------------------------------------- summary ---
_SENT_SPLIT = re.compile(r"(?<=[\.\!\?؟۔])\s+|\n+")
_llm_summarizer = None  # optional callable(text, title) -> str


def set_llm_summarizer(fn) -> None:
    global _llm_summarizer
    _llm_summarizer = fn


def summarize(text: str, title: str = "", max_sentences: int = 4) -> str:
    if _llm_summarizer is not None:
        try:
            out = _llm_summarizer(text, title)
            if out:
                return out.strip()
        except Exception:
            pass  # fall through to extractive
    text = (text or "").strip()
    if not text:
        return ""
    sentences = [s.strip() for s in _SENT_SPLIT.split(text) if len(s.strip()) > 20]
    if not sentences:
        return text[:300]
    # Extractive: rank sentences by keyword overlap, keep original order.
    kw = set(extract_keywords(text, 15))
    scored = []
    for idx, sent in enumerate(sentences[:40]):
        score = sum(1 for t in _tokens(sent) if t.lower() in kw)
        score += 1.5 if idx == 0 else 0  # lead bias
        scored.append((score, idx, sent))
    top = sorted(scored, key=lambda x: -x[0])[:max_sentences]
    return " ".join(s for _, _, s in sorted(top, key=lambda x: x[1]))


